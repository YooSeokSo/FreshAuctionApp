package com.example.freshauctionapp.result

import com.example.subway.util.SingleLiveEvent
import android.content.Context
import android.util.Log
import androidx.lifecycle.LiveData
import androidx.lifecycle.MutableLiveData
import androidx.lifecycle.ViewModel
import androidx.lifecycle.viewModelScope
import com.example.freshauctionapp.database.DatabaseModule
import com.example.freshauctionapp.model.FreshData
import com.example.freshauctionapp.model.FreshWrapper
import com.example.freshauctionapp.model.Fruits
import com.example.freshauctionapp.model.SaveItem
import com.example.freshauctionapp.network.NetworkModule
import com.squareup.moshi.Moshi
import com.squareup.moshi.kotlin.reflect.KotlinJsonAdapterFactory
import kotlinx.coroutines.CoroutineExceptionHandler
import kotlinx.coroutines.Dispatchers
import kotlinx.coroutines.launch
import ru.gildor.coroutines.okhttp.await

class ResultViewModel() : ViewModel() {

    // JSON 라이브러리 모시(moshi) 플러그인
    val moshi by lazy {
        //data class를 JSON처럼 다룰 수 있도록 해주는 Moshi 플러그인
        Moshi.Builder().add(KotlinJsonAdapterFactory()).build()
    }

    //notificationMsg LiveData 선언 - SingleLiveEvent() 선언
    val notificationMsg: SingleLiveEvent<String> = SingleLiveEvent()

    /* resultList LiveData 선언 - SingleLiveEvent()  */
    private val resultList: SingleLiveEvent<List<FreshData>> = SingleLiveEvent()

    //resultList() getter 선언
    fun resultList(): LiveData<List<FreshData>> = resultList as MutableLiveData<List<FreshData>>

    //검색 결과를 SaveItem 테이블과 Fresh 테이블에 저장
    fun saveResult(context: Context, saveName: String) {
        viewModelScope.launch(Dispatchers.IO) {
            /* SaveItem 테이블에 경락가격정보 저장
               - database Dao를 형태로 가져옵니다.
               - 먼저 저장 항목리스트를 만들어서 autoGenerated된 ID값을 받은다음,
               - freshdata(각각의 개별 데이터)를 리스트 ID값과 함께 저장해줍니다.
             */
            DatabaseModule.getDatabase(context).freshDao().insertSave(
                SaveItem(id = null, saveTitle = saveName)//saveName: "2020-06-15 사과 검색결과"
            ).run {
                //Fresh 테이블에 경락가격정보 저장
                resultList.value?.let { datas ->
                    datas.forEach { it.saveId = this }

                    Log.i("SAVERESULT", "datas: $datas")

                    /* FreshData(id=null, saveId=1, lname=과일과채류, mname=수박..cprName=동화청과....)
                       FreshData(id=null, saveId=1, lname=과일과채류, mname=수박..cprName=서울청과....)
                       ...
                       ** Fresh table에
                         - id(자동발급), saveId(1), lname=과일과채류, mname=수박..cprName=동화청과...
                         - id(자동발급), saveId(1), lname=과일과채류, mname=수박..cprName=동화청과...
                         .... 저장 됨
                     */

                    DatabaseModule.getDatabase(context).freshDao().insertFresh(datas)
                }
            }
        }
    }//end of saveResult

    val errorHandler = CoroutineExceptionHandler { _, exception ->
        Log.e("error", exception.message)
        notificationMsg.postValue(exception.message)
    }

    //Request 객체 생성 함수
    fun loadDataFromURL(
        selectDate: String,
        selectFruit: String,
        resultAmount: String
    ) {
        /* Request 객체 생성 */
        val httpUrl = NetworkModule.makeHttprequest(
            /* 분류코드(소분류), 검색일자, 검색수량을 인자로 HttpUrl 객체 생성 함수를 호출하여
               HttpUrl 객체 생성 */
            NetworkModule.makeHttpUrl(
                scode = Fruits.valueOf(selectFruit).scode,
                date = selectDate,
                amount = resultAmount
            )
        )

        Log.i("HTTP", httpUrl.toString())

        /* Coroutine을 이용하여 IO 스레드에서 경락가격정보서비스 서버에 요청  */
        viewModelScope.launch(Dispatchers.IO + errorHandler) {
             Log.i("FRESH", httpUrl.url.toString())

            /* response(응답) 객체 - 경락가격정보 검색 요청
               - OkHTTPClient.newCall()의 인자로 Request 객체(request)를 전달하여 실행(요청)
             */
            val response = NetworkModule.clinent.newCall(httpUrl).await()

            /* String을 Moshi를 이용 JSON Body로 파싱 */
            val freshData =
                response.body?.string()?.let { mapppingStringToFresh(it) } ?: emptyList()
            Log.i("FRESH", freshData[1].msg)
            if(freshData.isEmpty()){
                notificationMsg.postValue("데이터가 존재하지 않습니다.")
            }

            //resultList(LiveData) 저장
            resultList.postValue(freshData)
        }
    }//end of loadDataFromURL

    //Moshi를 이용 JSON Body로 파싱
    fun mapppingStringToFresh(jsonBody: String): List<FreshData> {
        // json 스트링을 데이터 클래스(FreshWrapper)에 맞게 자동으로 맵핑해주는 어댑터를 생성
        val FreshStringToJsonAdapter = moshi.adapter(FreshWrapper::class.java)

        //freshStringToJsonAdapter.fromJson() - String -> FreshWrapper?
        //freshStringToJsonAdapter.toJson() - FreshWrapper -> String
        val freshResponse = FreshStringToJsonAdapter.fromJson(jsonBody)//파싱
        Log.i("LIST", "${freshResponse}")//list=[FreshData(id=null, saveId=null, lname=과실류, mname=포도, ...)]

        return freshResponse?.list ?: emptyList()
    }//end of mapppingStringToFresh
}
